    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        JFrame parentFrame = new JFrame();
        File fileToSave=null;
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setSelectedFile(new File(generateFile.getName().substring(0,generateFile.getName().length()-4)+".txt"));
        fileChooser.setDialogTitle("Specify a file to save");
        fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("Text Document", "txt"));
        int userSelection = fileChooser.showSaveDialog(parentFrame);
        if (userSelection == JFileChooser.APPROVE_OPTION) {
            fileToSave = fileChooser.getSelectedFile();
            if(fileToSave==null){
                return;
            }
            rf.generateData(fileToSave.toPath().toString());
        }
    }//GEN-LAST:event_btnGenerateActionPerformed

